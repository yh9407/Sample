

public class IfElseIfElseExample {
	public static void main(String[] args) {
		int workingTime = 7; // 근무시간
		
		if (workingTime>=8 ) {
			System.out.println("퇴근~");
		}
			else {
				System.out.println("계속 근무!");
			}
		}

		 // 코드 작성
		 // workingTime 이 8 이상이면 퇴근
		 // workingTime 이 8 미만이면 계속 근무

	}

